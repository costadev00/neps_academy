import math

#teto
print(math.ceil(2.5))

#floor
print(math.floor(2.5))

#factorial
print(math.factorial(5))

#power (ele pega os dois parametros, tranforma em float e calcula a potencia)
print(math.pow(2,3))

#potencia sem transformacao em flaot
print(2**3)

#sqrt
print(math.sqrt(25))

#gcd
print(math.gcd(12,18))
