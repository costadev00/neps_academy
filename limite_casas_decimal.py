x = 4
y = 3

print("O valor da divisão é {:.2f}".format(x / y)) # Seria impresso 1.33
print("O valor da divisão é {:.3f}".format(x / y)) # Seria impresso 1.333
print("O valor da divisão é {:.7f}".format(x / y)) # Seria impresso 1.3333333
print("O valor da divisão é {:.0f}".format(x / y)) # Seria impresso 1
